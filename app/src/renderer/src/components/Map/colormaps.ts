function fromRgbData(data: (typeof rgbData)[keyof typeof rgbData]): Colormap {
  return (r: number) => {
    if (r <= 0) return data.at(0)!
    if (r >= 1) return data.at(-1)!
    const pos = r * (data.length - 1)
    const int = Math.trunc(pos)
    const frac = pos - int
    const min = data[int]
    const max = data[int + 1]
    return [
      max[0] * frac + min[0] * (1 - frac),
      max[1] * frac + min[1] * (1 - frac),
      max[2] * frac + min[2] * (1 - frac)
    ]
  }
}

const gistHeat: Colormap = (r: number) => {
  const R = Math.min(1, Math.max(0, 1.5 * r))
  const G = Math.min(1, Math.max(0, 2 * r - 1))
  const B = Math.min(1, Math.max(0, 4 * r - 3))
  return [R, G, B]
}

